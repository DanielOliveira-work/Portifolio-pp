let currentLang = localStorage.getItem('lang') || 'pt';

const EMAILJS_SERVICE_ID = 'service_lj0aixv';
const EMAILJS_TEMPLATE_ID = 'template_i7kqjr3';
const EMAILJS_PUBLIC_KEY = 'gOw9HU4lDh11J3jji';

function applyTranslations(lang) {
  const dict = translations[lang];
  if (!dict) return;

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    const attr = el.getAttribute('data-i18n-attr');
    const isHtml = el.getAttribute('data-i18n-html') === 'true';
    const value = dict[key];

    if (!value) return;

    if (attr) {
      el.setAttribute(attr, value);
    } else if (isHtml) {
      el.innerHTML = value;
    } else {
      el.textContent = value;
    }
  });
}

function setupScheduleForm() {
  const form = document.querySelector('.form-grid');
  if (!form) return;

  const statusEl = document.querySelector('[data-form-status]');
  const submitBtn = form.querySelector('.schedule-submit');

  form.addEventListener('submit', (event) => {
    event.preventDefault();

    const firstName = form.firstName?.value.trim();
    const lastName = form.lastName?.value.trim();
    const phone = form.phone?.value.trim();
    const email = form.email?.value.trim();
    const contactPreference = form.contactPreference?.value || 'email';

    if (!firstName || !phone || !email) {
      if (statusEl) {
        statusEl.textContent = translations[currentLang]['schedule.form.status.validation'];
        statusEl.className = 'form-status is-error';
      }
      return;
    }

    if (!window.emailjs || !EMAILJS_SERVICE_ID || !EMAILJS_TEMPLATE_ID || !EMAILJS_PUBLIC_KEY) {
      if (statusEl) {
        statusEl.textContent = translations[currentLang]['schedule.form.status.error'];
        statusEl.className = 'form-status is-error';
      }
      return;
    }

    if (submitBtn) {
      submitBtn.disabled = true;
    }

    if (statusEl) {
      statusEl.textContent = '';
      statusEl.className = 'form-status';
    }

    if (window.emailjs && EMAILJS_PUBLIC_KEY) {
      window.emailjs.init(EMAILJS_PUBLIC_KEY);
    }

    const templateParams = {
      user_name: firstName,
      user_surname: lastName,
      user_phone: phone,
      user_email: email,
      contactPreference,
    };

    window.emailjs
      .send(EMAILJS_SERVICE_ID, EMAILJS_TEMPLATE_ID, templateParams)
      .then(() => {
        if (statusEl) {
          statusEl.textContent = translations[currentLang]['schedule.form.status.success'];
          statusEl.className = 'form-status is-success';
        }
        form.reset();
      })
      .catch(() => {
        if (statusEl) {
          statusEl.textContent = translations[currentLang]['schedule.form.status.error'];
          statusEl.className = 'form-status is-error';
        }
      })
      .finally(() => {
        if (submitBtn) {
          submitBtn.disabled = false;
        }
      });
  });
}

function setupLangSwitch() {
  const buttons = document.querySelectorAll('.lang-option');

  // Restaura língua salva
  const saved = localStorage.getItem('lang') || 'pt';
  if (saved !== currentLang) {
    currentLang = saved;
    buttons.forEach((b) => b.classList.toggle('is-active', b.getAttribute('data-lang') === saved));
  }

  buttons.forEach((btn) => {
    btn.addEventListener('click', () => {
      const lang = btn.getAttribute('data-lang');
      if (lang === currentLang) return;
      currentLang = lang;
      localStorage.setItem('lang', lang);
      buttons.forEach((b) => b.classList.toggle('is-active', b === btn));
      applyTranslations(lang);
    });
  });
}

function setupSmoothScroll() {
  document.querySelectorAll('[data-scroll]').forEach((btn) => {
    btn.addEventListener('click', () => {
      const target = btn.getAttribute('data-scroll');
      const el = document.querySelector(target);
      if (el) {
        el.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }
    });
  });
}

function setupSlider() {
  const slider = document.querySelector('[data-slider]');
  if (!slider) return;

  const track = slider.querySelector('.slider-track');
  const slides = Array.from(slider.querySelectorAll('[data-slide]'));
  const prevBtn = slider.querySelector('[data-slider-prev]');
  const nextBtn = slider.querySelector('[data-slider-next]');
  const dotsContainer = slider.querySelector('[data-slider-dots]');

  if (!track || !slides.length) return;

  let currentIndex = 0;

  function goToSlide(index) {
    if (index < 0) index = slides.length - 1;
    if (index >= slides.length) index = 0;
    currentIndex = index;

    const targetSlide = slides[currentIndex];
    const offset = targetSlide.offsetLeft;
    track.style.transform = `translateX(-${offset}px)`;

    if (dotsContainer) {
      Array.from(dotsContainer.children).forEach((dot, i) => {
        dot.classList.toggle('is-active', i === currentIndex);
      });
    }
  }

  function createDots() {
    if (!dotsContainer) return;
    dotsContainer.innerHTML = '';
    slides.forEach((_, index) => {
      const dot = document.createElement('button');
      dot.type = 'button';
      dot.className = 'slider-dot';
      dot.setAttribute('aria-label', `Ir para o slide ${index + 1}`);
      dot.addEventListener('click', () => goToSlide(index));
      dotsContainer.appendChild(dot);
    });
  }

  if (prevBtn) {
    prevBtn.addEventListener('click', () => {
      goToSlide(currentIndex - 1);
    });
  }

  if (nextBtn) {
    nextBtn.addEventListener('click', () => {
      goToSlide(currentIndex + 1);
    });
  }

  // Drag / swipe support
  let dragStartX = 0;
  let isDragging = false;

  function onDragStart(x) {
    dragStartX = x;
    isDragging = true;
    track.style.transition = 'none';
  }

  function onDragEnd(x) {
    if (!isDragging) return;
    isDragging = false;
    track.style.transition = '';
    const diff = dragStartX - x;
    if (Math.abs(diff) > 50) {
      goToSlide(diff > 0 ? currentIndex + 1 : currentIndex - 1);
    } else {
      goToSlide(currentIndex);
    }
  }

  // Mouse
  track.addEventListener('mousedown', (e) => onDragStart(e.clientX));
  window.addEventListener('mouseup', (e) => onDragEnd(e.clientX));
  track.addEventListener('dragstart', (e) => e.preventDefault());

  // Touch
  track.addEventListener('touchstart', (e) => onDragStart(e.touches[0].clientX), { passive: true });
  track.addEventListener('touchend', (e) => onDragEnd(e.changedTouches[0].clientX));

  let resizeTimer;
  window.addEventListener('resize', () => {
    clearTimeout(resizeTimer);
    resizeTimer = setTimeout(() => goToSlide(currentIndex), 100);
  });

  createDots();
  goToSlide(0);
}

function setupThemeToggle() {
  const btn = document.getElementById('themeToggle');
  if (!btn) return;

  function applyTheme(theme) {
    document.documentElement.setAttribute('data-theme', theme);
    document.body.setAttribute('data-theme', theme);
    document.documentElement.style.colorScheme = theme;
    localStorage.setItem('theme', theme);

    // injeta/atualiza style tag para forçar cor em tempo real
    let styleTag = document.getElementById('theme-override');
    if (!styleTag) {
      styleTag = document.createElement('style');
      styleTag.id = 'theme-override';
      document.head.appendChild(styleTag);
    }
    if (theme === 'light') {
      styleTag.textContent = `
        *:not(.footer-links a):not(.footer-links a *):not(.footer-links):not(#splash):not(#splash *):not(.proj-card):not(.proj-card *) { color: #0d0f1a !important; }
        .hero-highlight, .hero-badge { color: #ff335c !important; }
        .hero-primary, .schedule-submit { color: #08090f !important; }
        .lang-option.is-active { color: #ffffff !important; }
        .footer-links a { color: rgba(245,245,245,0.7) !important; }
        .footer-links a:hover { color: #ff335c !important; }
        .proj-card-title, .proj-card-cat, .proj-card-label { color: #ffffff !important; }
      `;
    } else {
      styleTag.textContent = '';
    }
  }

  const saved = localStorage.getItem('theme') || 'dark';
  applyTheme(saved);

  btn.addEventListener('click', () => {
    const current = document.documentElement.getAttribute('data-theme') || 'dark';
    applyTheme(current === 'dark' ? 'light' : 'dark');
  });
}

window.addEventListener('DOMContentLoaded', () => {
  setupLangSwitch();
  setupSmoothScroll();
  setupScheduleForm();
  setupSlider();
  setupThemeToggle();
  applyTranslations(currentLang);
});